--  29.Category Performance Ranking
SELECT pt.category,SUM(od.quantity*p.price) AS total_revenue,
RANK() OVER ( ORDER BY SUM(od.quantity*p.price) DESC) AS category_rank
FROM order_details od
Join pizzas p ON 
od.pizza_id=p.pizza_id
join pizza_types pt ON
pt.pizza_type_id=p.pizza_type_id
