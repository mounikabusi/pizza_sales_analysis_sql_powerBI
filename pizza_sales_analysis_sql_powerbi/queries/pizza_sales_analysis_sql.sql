-- 21.Sales by Time of Day
SELECT
CASE
WHEN HOUR(time)<12 THEN 'Morning'
WHEN HOUR(time) BETWEEN 12 AND 17 THEN 'Afternoon'
ELSE 'Evening'
END AS time_of_day,
COUNT(order_id) as total_orders
FROM orders
GROUP BY time_of_day
ORDER BY total_orders DESC
;

-- 22.running revenue
SELECT o.date,SUM(od.quantity*p.price) AS total_revenue,
SUM(
SUM(od.quantity*p.price))
OVER(ORDER BY o.date) AS running_revenue
FROM order_details od
JOIN pizzas p ON 
od.pizza_id=p.pizza_id
JOIN orders o ON
od.order_id=o.order_id
GROUP BY o.date;

-- 23.rank pizzas by revenue
SELECT pt.name,SUM(od.quantity*p.price) AS total_revenue,
RANK() OVER (ORDER BY SUM(od.quantity*p.price) DESC) AS pizza_rank
FROM order_details od
Join pizzas p ON 
od.pizza_id=p.pizza_id
join pizza_types pt ON
pt.pizza_type_id=p.pizza_type_id
GROUP BY pt.name;

-- 24.Top Pizza in Each Category
WITH ranked_pizzas AS(
SELECT pt.name,pt.category,SUM(od.quantity*p.price) AS total_revenue,
RANK() OVER (partition by  pt.category ORDER BY SUM(od.quantity*p.price) DESC) AS pizza_rank
FROM order_details od
Join pizzas p ON 
od.pizza_id=p.pizza_id
join pizza_types pt ON
pt.pizza_type_id=p.pizza_type_id
GROUP BY pt.category,pt.name
)
SELECT * FROM ranked_pizzas
WHERE pizza_rank=1 ;

-- 25.Revenue Contribution Percentage
SELECT pt.category,ROUND(SUM(od.quantity*p.price)) AS total_revenue,

ROUND((SUM(od.quantity*p.price)/
(SELECT (SUM(od2.quantity*p2.price)) FROM
order_details od2 JOIN pizzas p2 ON
od2.pizza_id=p2.pizza_id))*100) AS total_percentage
FROM order_details od 
JOIN pizzas p ON 
od.pizza_id=p.pizza_id
JOIN pizza_types pt ON
p.pizza_type_id=pt.pizza_type_id
GROUP BY pt.category
ORDER BY total_revenue;

-- 26. Top 3 Pizzas Per Category
WITH ranked_pizzas AS(
SELECT pt.name,pt.category,SUM(od.quantity*p.price) AS total_revenue,
RANK() OVER (partition by  pt.category ORDER BY SUM(od.quantity*p.price) DESC) AS pizza_rank
FROM order_details od
Join pizzas p ON 
od.pizza_id=p.pizza_id
join pizza_types pt ON
pt.pizza_type_id=p.pizza_type_id
GROUP BY pt.category,pt.name
)
SELECT * FROM ranked_pizzas
WHERE pizza_rank<=3 ;

