-- 30.Pizza Sales Segmentation
SELECT pt.name,pt.category,SUM(od.quantity*p.price) AS total_revenue,
CASE
WHEN SUM(od.quantity * p.price) > 30000 THEN 'High Performing'
WHEN SUM(od.quantity * p.price) BETWEEN 15000 AND 30000 THEN 'Medium Performing'
ELSE 'Low Performing'END AS performance_segment
FROM order_details od
Join pizzas p ON 
od.pizza_id=p.pizza_id
join pizza_types pt ON
pt.pizza_type_id=p.pizza_type_id
GROUP BY pt.name,pt.category
ORDER BY performance_segment ASC;

-- 31.moving average analysis
WITH daily_sales AS(
SELECT o.date,SUM(od.quantity*p.price) AS total_revenue
FROM order_details od
JOIN orders o ON
o.order_id=od.order_id
Join pizzas p ON 
od.pizza_id=p.pizza_id GROUP BY o.date )
SELECT date,total_revenue,AVG(total_revenue) OVER(ORDER BY date ROWS BETWEEN 6 PRECEDING AND CURRENT ROW) AS moving_avg FROM daily_sales;

-- 32.Revenue Growth Rate
WITH monthly_sales AS(
SELECT  MONTH(o.date) AS month_no,MONTHNAME(o.date) AS month_name,SUM(od.quantity*p.price) AS total_revenue
FROM order_details od
JOIN orders o ON
o.order_id=od.order_id
Join pizzas p ON 
od.pizza_id=p.pizza_id GROUP BY month_name,month_no )
SELECT month_no,month_name,total_revenue,
LAG(total_revenue ) OVER (ORDER BY  month_no ) AS previous_month_sales,

ROUND((total_revenue -LAG(total_revenue) OVER(ORDER BY month_no))
/LAG(total_revenue) OVER(ORDER BY month_no)* 100,2) 
AS growth_percentage
 FROM monthly_sales ;
